    #     f_adj = f_adj.scatter_(1, choose_k, 1)
    #     torch.save(f_adj, f_path)
    # else:
    #     f_adj = torch.load(f_path)

    return features, nfeats, labels, nclasses, train_mask, val_mask, test_mask, nsadj, nfadj


